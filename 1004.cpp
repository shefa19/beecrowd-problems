//beecrowd 1004 - Simple Product
#include<iostream>
using namespace std;

int main()
{
    int num1, num2, PROD;

    //Input the value of num1 and num2
    cin >> num1;
    cin >> num2;

    //Calculation the product of num1 and num2
    PROD = num1 * num2;

    //Print the value of PROD
    cout << "PROD = " << PROD << endl;


    return 0;
}
