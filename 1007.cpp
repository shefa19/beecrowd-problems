#include<iostream>
using namespace std;

int main()
{
    int A, B, C, D, Dif;

    //Input
    cin >> A;
    cin >> B;
    cin >> C;
    cin >> D;

    //Difference calculation
    Dif = (A * B) - (C * D);

    //Print
    cout << "DIFERENCA = " << Dif << endl;



    return 0;
}
